function hideElem() {
    var x = document.getElementById("menuButtons");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function hidePlay() {
    var x = document.getElementById("choosePlayers");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}